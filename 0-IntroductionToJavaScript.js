/* *** Wats is javascript *** */

/* 
JavaScript, a menudo abreviado como JS, es un lenguaje de programación que es una de las tecnologías 
centrales de la World Wide Web, junto con HTML y CSS. Nos permite agregar interactividad a las páginas; por 
ejemplo, es posible que haya visto controles deslizantes, alertas, interacciones de clic, ventanas 
emergentes, etc. en diferentes sitios web; todo eso se crea con JavaScript. Además de usarse en el 
navegador, también se usa en otros entornos que no son de navegador, como Node.js para escribir código del 
lado del servidor en JavaScript, Electron para escribir aplicaciones de escritorio, React Native para 
aplicaciones móviles, etc.
*/

/* *** History of JavaScript *** */

/* 
JavaScript fue creado inicialmente por Brendan Eich de NetScape y fue anunciado por primera vez en un 
comunicado de prensa de Netscape en 1995. Tiene una extraña historia de nombres; inicialmente, el creador 
lo llamó Mocha, que luego fue renombrado LiveScript. En 1996, aproximadamente un año después del 
lanzamiento, NetScape decidió cambiarle el nombre a JavaScript con la esperanza de sacar provecho de la 
comunidad Java (aunque JavaScript no tenía ninguna relación con Java) y lanzó Netscape 2.0 con el soporte 
oficial de JavaScript.
*/

/* *** JavaScript versions *** */

/* 
JavaScript, inventado por Brendan Eich, alcanzó el estatus de estándar ECMA en 1997 y adoptó el nombre 
oficial ECMAScript. Este lenguaje ha evolucionado a través de varias versiones, a saber, ES1, ES2, ES3, ES5 
y la versión transformativa ES6. Estas actualizaciones han desempeñado un papel crucial en la mejora y 
estandarización de JavaScript, lo que lo ha convertido en un lenguaje ampliamente utilizado y valioso en el 
cambiante campo del desarrollo web.
*/

